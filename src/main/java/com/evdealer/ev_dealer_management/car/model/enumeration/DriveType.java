package com.evdealer.ev_dealer_management.car.model.enumeration;

public enum DriveType {
    FWD,       // Front-Wheel Drive -> Dẫn động cầu trước
    RWD,       // Rear-Wheel Drive -> Dẫn động cầu sau
    AWD,       // All-Wheel Drive -> Dẫn động 4 bánh toàn thời gian (tự động phân bổ lực kéo)
    FOUR_WD    // Four-Wheel Drive -> Dẫn động 4 bánh bán thời gian (có chế độ gài cầu, thường dùng off-road)
}
